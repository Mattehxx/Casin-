    /**
     * Metodo per avviare la slotmachine
     * @param evt bottone cliccato 
     */
    private void btnSpinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSpinActionPerformed
        // TODO add your handling code here:
        //se i crediti del profilo sono esauriti viene visualizzato il popup
        if (lblNumCredits.getText().equals("0") && lblBet.getText().equals("0")) {
            JOptionPane.showMessageDialog(null, "Hai esaurito i crediti !!!", "Attenzione", JOptionPane.WARNING_MESSAGE);
        }
        //se la puntata Ã¨ 0 non esegue spin
        if (lblBet.getText().equals("0")) {
            return;
        }

        spin1=new Spin(images, lblImage1, 200);
        spin2=new Spin(images, lblImage2, 300);
        spin3=new Spin(images, lblImage3, 400);
        spin4=new Spin(images, lblImage4, 500);
        spin5=new Spin(images, lblImage5, 600);
        
        spin1.start();
        spin2.start();
        spin3.start();
        spin4.start();
        spin5.start();
     
        //attesa che i thread abbiano terminato la loro esecuzione e verifica delle condizioni di vittoria
        ConditionSlotMachine csm=new ConditionSlotMachine(spin1, spin2, spin3, spin4, spin5, lblBet, lblNumCredits, lblNumWinnings, btnSpin, initialCredits);
        csm.start();
    }//GEN-LAST:event_btnSpinActionPerformed

    private void btnPlus1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlus1ActionPerformed
        // TODO add your handling code here:
        //controllo crediti account e crediti parziali vinti con slot machine
        if (Integer.parseInt(lblNumCredits.getText())>=1) {
            int tmp=Integer.parseInt(lblBet.getText())+1;
            lblBet.setText(String.valueOf(tmp));
            
            tmp=Integer.parseInt(lblNumCredits.getText())-1;
            lblNumCredits.setText(String.valueOf(tmp));
        }
    }//GEN-LAST:event_btnPlus1ActionPerformed

